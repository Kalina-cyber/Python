#Система управління задачами
tasks = {
    "Прибрати кімнату": "виконано",
    "Купити продукти": "в процесі",
    "Написати звіт": "очікує",
    "Підготувати презентацію": "очікує"
} # Створюємо словник задач

# Функція для додавання нової задачі
def add_task(name, status="очікує"):
    if name not in tasks:  # Перевіряємо, чи такої задачі ще немає
        tasks[name] = status  # Додаємо нову задачу зі статусом
    else:
        print(f"Задача '{name}' вже існує!")

# Функція для видалення задачі
def remove_task(name):
    if name in tasks:  # Якщо задача є в списку
        del tasks[name]  # Видаляємо її
    else:
        print(f"Задача '{name}' не знайдена!")

# Функція для оновлення статусу задачі
def update_task_status(name, new_status):
    if name in tasks:  # Перевіряємо, чи існує така задача
        tasks[name] = new_status  # Оновлюємо статус
    else:
        print(f"Задача '{name}' не знайдена!")

# Функція для отримання списку задач зі статусом "очікує"
def pending_tasks():
    pending = []  # Порожній список для збереження задач, які "очікують"

    for task, status in tasks.items():  # Перебираємо всі задачі
        if status == "очікує":  # Якщо статус - "очікує"
            pending.append(task)  # Додаємо задачу в список

    return pending  # Повертаємо список задач, що очікують виконання

# Приклад використання
if __name__ == "__main__":
    print("Актуальний список задач:", tasks)

    add_task("Зробити домашнє завдання")  # Додаємо нову задачу (статус "очікує")
    update_task_status("Купити продукти", "виконано")  # Оновлюємо статус
    remove_task("Прибрати кімнату")  # Видаляємо задачу

    print("Змінений список задач:", tasks)
    print("Задачі, що очікують виконання:", pending_tasks())
