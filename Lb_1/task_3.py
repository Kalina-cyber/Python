#Статистика продажів
# Список продажів: кожен елемент - це словник з інформацією про продаж
sales = [
    {"продукт": "яблука", "кількість": 50, "ціна": 10},
    {"продукт": "банани", "кількість": 20, "ціна": 15},
    {"продукт": "груші", "кількість": 40, "ціна": 25},
    {"продукт": "апельсини", "кількість": 30, "ціна": 8},
    {"продукт": "виноград", "кількість": 100, "ціна": 12}
]

# Функція для підрахунку доходу
def calculate_revenue(sales_list):
    revenue = {}  # Порожній словник для збереження доходів

    for sale in sales_list:  # Проходимося по всіх продажах
        product = sale["продукт"]  # Отримуємо назву продукту
        quantity = sale["кількість"]  # Отримуємо кількість проданих одиниць
        price = sale["ціна"]  # Отримуємо ціну за одиницю

        # Обчислюємо загальний дохід за цей продукт
        if product in revenue:
            revenue[product] += quantity * price
        else:
            revenue[product] = quantity * price

    return revenue  # Повертаємо словник з доходами

# Отримуємо загальний дохід для кожного продукту
total_revenue = calculate_revenue(sales)

# Отримуємо список продуктів, що принесли дохід більше 1000
high_revenue_products = []  # Створюємо порожній список

for product, income in total_revenue.items():  # Перебираємо кожен продукт
    if income > 1000:  # Якщо дохід більший за 1000
        high_revenue_products.append(product)  # Додаємо продукт у список

# Виводимо результати
print("Загальний дохід по продуктах:", total_revenue)
print("Продукти з доходом понад 1000:", high_revenue_products)
