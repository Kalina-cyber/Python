# Функція для підрахунку частоти слів у тексті
def word_count(text):
    words = text.lower().split()  # Перетворюємо текст у нижній регістр і розбиваємо його на окремі слова
    word_freq = {}  # Створюємо порожній словник для збереження частоти слів

    for word in words:  # Проходимося по кожному слову у списку
        if word in word_freq:
            word_freq[word] += 1  # Якщо слово вже є в словнику, збільшуємо його лічильник
        else:
            word_freq[word] = 1  # Якщо слова немає, додаємо його в словник із початковим значенням 1

    return word_freq  # Повертаємо словник із частотою слів


# Функція для отримання слів, що зустрічаються більше 3 разів
def frequent_words(word_freq, threshold=3):
    result = []  # Створюємо порожній список для збереження потрібних слів
    for word, count in word_freq.items():  # Перебираємо всі пари слово-кількість у словнику
        if count > threshold:
            result.append(word)  # Якщо слово зустрічається більше вказаного порогу, додаємо його до списку
    return result  # Повертаємо список знайдених слів


